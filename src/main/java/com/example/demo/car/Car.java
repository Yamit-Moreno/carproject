package com.example.demo.car;

/**
 * Interface representing a car.
 * Defines core car operations: start, drive, and stop.
 */
public interface Car {
    /**
     * Starts the car's engine.
     */
    void start();

    /**
     * Drives the car.
     */
    void drive();

    /**
     * Stops the car's engine.
     */
    void stop();
}
